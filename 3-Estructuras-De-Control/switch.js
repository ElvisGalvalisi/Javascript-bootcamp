// switch

let nota = 5;

switch(nota){
    case 5:
        console.log("tu nota es un 5");
        break;
    case 4:
        console.log("tu nota es un 4");
        break;
    case 3:
        console.log("tu nota es un 3");
        break;
    case 2:
        console.log("tu nota es un 2");
        break;
    case 1:
         console.log("tu nota es un 1");
         break;
    default: 
        console.log("Caso por defecto, poner siempre un default en los switch"); 
    break;
}